func TestAssessCompatibleCleanAndPlan(t *testing.T) {
	manifestRoot := generatorRootForUpgrade(t)
	projectDir := generateProjectForUpgrade(t, manifestRoot, "light")

	withGeneratorIdentity(t, "v0.14.0", "phase14-current", func() {
		assessment, err := Inspect(projectDir, manifestRoot)
		if err != nil {
			t.Fatalf("Inspect() returned error: %v", err)
		}
		if assessment.DiffStatus != metadata.StatusClean {
			t.Fatalf("expected clean diff status, got %q", assessment.DiffStatus)
		}
		if assessment.CompatibilityLevel != LevelCompatible {
			t.Fatalf("expected compatible level, got %q", assessment.CompatibilityLevel)
		}

		plan, err := Plan(projectDir, manifestRoot)
		if err != nil {
			t.Fatalf("Plan() returned error: %v", err)
		}
		if len(plan.RecommendedSteps) != 1 || !strings.Contains(plan.RecommendedSteps[0], "无需升级动作") {
			t.Fatalf("expected no-op upgrade plan, got %#v", plan.RecommendedSteps)
		}
	})
}

func TestAssessManualReviewForLocalModified(t *testing.T) {
	manifestRoot := generatorRootForUpgrade(t)
	projectDir := generateProjectForUpgrade(t, manifestRoot, "light")

	readmePath := filepath.Join(projectDir, "README.md")
	readmeData, err := os.ReadFile(readmePath)
	if err != nil {
		t.Fatalf("read generated README: %v", err)
	}
	if err := os.WriteFile(readmePath, append(readmeData, []byte("\nlocal change\n")...), 0o644); err != nil {
		t.Fatalf("write local change README: %v", err)
	}

	withGeneratorIdentity(t, "v0.14.0", "phase14-current", func() {
		assessment, err := Inspect(projectDir, manifestRoot)
		if err != nil {
			t.Fatalf("Inspect() returned error: %v", err)
		}
		if assessment.DiffStatus != metadata.StatusLocalModified {
			t.Fatalf("expected local_modified diff status, got %q", assessment.DiffStatus)
		}
		if assessment.CompatibilityLevel != LevelManualReview {
			t.Fatalf("expected manual_review level, got %q", assessment.CompatibilityLevel)
		}
		if len(assessment.LocalModifiedFiles) == 0 || assessment.LocalModifiedFiles[0] != "README.md" {
			t.Fatalf("expected README.md in local modified files, got %#v", assessment.LocalModifiedFiles)
		}
	})
}

func TestAssessCompatibleForGeneratorDrift(t *testing.T) {
	originalRoot := generatorRootForUpgrade(t)
	generatorCopy := filepath.Join(t.TempDir(), "generator")
	copyDirForUpgrade(t, originalRoot, generatorCopy)

	baseReadmePath := filepath.Join(generatorCopy, "assets", "base", "service-base-cobra", "README.md.tmpl")
	baseReadmeData, err := os.ReadFile(baseReadmePath)
	if err != nil {
		t.Fatalf("read copied generator README template: %v", err)
	}
	updatedReadme := strings.Replace(string(baseReadmeData), "Generated by `fiberx`.", "Generated by `fiberx` (phase14 drift).", 1)
	if err := os.WriteFile(baseReadmePath, []byte(updatedReadme), 0o644); err != nil {
		t.Fatalf("write copied generator README template: %v", err)
	}

	projectDir := generateProjectForUpgrade(t, generatorCopy, "light")

	withGeneratorIdentity(t, "v0.14.0", "phase14-current", func() {
		assessment, err := Inspect(projectDir, originalRoot)
		if err != nil {
			t.Fatalf("Inspect() returned error: %v", err)
		}
		if assessment.DiffStatus != metadata.StatusGeneratorDrift {
			t.Fatalf("expected generator_drift diff status, got %q", assessment.DiffStatus)
		}
		if assessment.CompatibilityLevel != LevelCompatible {
			t.Fatalf("expected compatible level, got %q", assessment.CompatibilityLevel)
		}
		if len(assessment.GeneratorDriftFiles) == 0 || assessment.GeneratorDriftFiles[0] != "README.md" {
			t.Fatalf("expected README.md in generator drift files, got %#v", assessment.GeneratorDriftFiles)
		}
	})
}

